    public final void atualizarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) TableClientes.getModel();
        modelo.setRowCount(0); // Limpa linhas padrão para receber os dados limpos
        
        ClienteController controller = new ClienteController();
        ArrayList<Cliente> listaClientes = controller.listar();
        
        for (Cliente cliente : listaClientes) {
            Object[] linha = new Object[]{
                cliente.getNomeEmpresa(), // Coluna 1
                cliente.getCnpj(),        // Coluna 2
                cliente.getTelefone(),    // Coluna 3
                cliente.getEmail()        // Coluna 4
            };
            modelo.addRow(linha);
        }
    }

