    private void txtPesquisarClienteKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisarClienteKeyReleased
        // TODO add your handling code here:
        
        String termoBusca = txtPesquisarCliente.getText().toLowerCase().trim();
        
        
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) TableClientes.getModel();
        modelo.setRowCount(0);
        
        
        Controller.ClienteController controller = new Controller.ClienteController();
        java.util.ArrayList<Model.Cliente> listaClientes = controller.listar();
        
        
        for (Model.Cliente cliente : listaClientes) {
            
            if (termoBusca.isEmpty() || cliente.getNomeEmpresa().toLowerCase().contains(termoBusca)) {
                Object[] linha = new Object[]{
                    cliente.getNomeEmpresa(),
                    cliente.getCnpj(),
                    cliente.getTelefone(),
                    cliente.getEmail()
                };
                modelo.addRow(linha); // Adiciona apenas o cliente filtrado na tela
            }
        }
    }//GEN-LAST:event_txtPesquisarClienteKeyReleased


