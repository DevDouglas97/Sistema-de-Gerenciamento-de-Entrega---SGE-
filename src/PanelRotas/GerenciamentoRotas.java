    private void VisualizarRotabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VisualizarRotabtnActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tableRotas.getSelectedRow();
        
        if (linhaSelecionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione uma rota na tabela para visualizar!");
            return;
        }
        
        Controller.RotasController controller = new Controller.RotasController();
        Model.Rotas rotaSelecionada = controller.listar().get(linhaSelecionada);
        
        Telas.Dashboard dashboard = (Telas.Dashboard) javax.swing.SwingUtilities.getWindowAncestor(this);
        dashboard.abrirPainel(new VisualizarRota(rotaSelecionada));
                                                     
    
    }//GEN-LAST:event_VisualizarRotabtnActionPerformed

