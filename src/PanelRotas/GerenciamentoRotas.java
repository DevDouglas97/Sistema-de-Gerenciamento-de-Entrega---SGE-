    private void cbFiltraStatusItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbFiltraStatusItemStateChanged
        // TODO add your handling code here:
        
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
            
            
            String statusSelecionado = cbFiltraStatus.getSelectedItem().toString().trim();
            
            
            DefaultTableModel modelo = (DefaultTableModel) tableRotas.getModel();
            modelo.setRowCount(0);
            
            
            RotasController controller = new RotasController();
            ArrayList<Rotas> lista = controller.listar();
            
            
            tableRotas.getColumnModel().getColumn(4).setCellRenderer(new RotasStatusRenderer());
            
            
            for (Rotas rota : lista) {
               
                if ("Todos".equalsIgnoreCase(statusSelecionado) || rota.getStatus().equalsIgnoreCase(statusSelecionado)) {
                    Object[] linha = new Object[]{
                        rota.getPedidoDisplay(),
                        rota.getCliente(),
                        rota.getEnderecoEntrega(),
                        rota.getMotorista(),
                        rota.getStatus()
                    };
                    modelo.addRow(linha);}
            }
        }
    }//GEN-LAST:event_cbFiltraStatusItemStateChanged


