    private void SalvarRotabtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarRotabtnActionPerformed
        // TODO add your handling code here:
        
        if (cbSelecionaPedido.getSelectedIndex() <= 0 && this.rotaExistente == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione um pedido válido!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (cbSelecionaEndereco.getSelectedIndex() <= 0 && this.rotaExistente == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione um endereço de entrega válido!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        
        Model.Rotas rotaObj;
        if (this.rotaExistente == null) {
            rotaObj = new Model.Rotas(); // Nova Rota
            
            
            String textoCombo = cbSelecionaPedido.getSelectedItem().toString();
            String pedidoDisplay = textoCombo.split(" - ")[0]; 
            
            rotaObj.setPedidoDisplay(pedidoDisplay);
            rotaObj.setCliente(txtCliente.getText());
            rotaObj.setMotorista(txtMotorista.getText());
            rotaObj.setEnderecoEntrega(cbSelecionaEndereco.getSelectedItem().toString());
            rotaObj.setStatus("Pendente"); // Regra de negócio: nascer automático como Pendente
        } else {
            rotaObj = this.rotaExistente; // Edita a rota existente
            rotaObj.setStatus(cbStatusEntrega.getSelectedItem().toString()); // Atualiza o status escolhido
        }

        
        if (this.rotaExistente == null) {
            Controller.RotasController controller = new Controller.RotasController();
            controller.salvar(rotaObj);
        }

        
        javax.swing.JOptionPane.showMessageDialog(this, "Rota gravada com sucesso!");

        
        Telas.Dashboard dashboard = (Telas.Dashboard) javax.swing.SwingUtilities.getWindowAncestor(this);
        dashboard.abrirPainel(new PanelRotas.GerenciamentoRotas());
    }//GEN-LAST:event_SalvarRotabtnActionPerformed

