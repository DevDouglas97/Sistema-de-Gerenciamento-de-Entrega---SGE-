    private void btnSalvarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarPedidoActionPerformed
        // TODO add your handling code here:
        
        if (cbSelecionaCliente.getSelectedIndex() <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione um cliente para o pedido!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (cbSelecionaMotorista.getSelectedIndex() <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, atribua um motorista para esta expedição!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

       
        javax.swing.table.DefaultTableModel modeloTabela = (javax.swing.table.DefaultTableModel) tableProdutosPedidosAdc.getModel();
        int totalLinhas = modeloTabela.getRowCount();
        
        if (totalLinhas == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, adicione pelo menos um produto ao pedido antes de salvar!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        
        Model.Expedicao novoPedido = new Model.Expedicao();
        novoPedido.setCliente(cbSelecionaCliente.getSelectedItem().toString());
        novoPedido.setMotorista(cbSelecionaMotorista.getSelectedItem().toString());
        novoPedido.setStatus("Aguardando"); // Atribuição automática exigida por padrão

       
        for (int i = 0; i < totalLinhas; i++) {
          
            int qtd = (int) modeloTabela.getValueAt(i, 0);
            String nomeProd = modeloTabela.getValueAt(i, 1).toString();
            String unidade = modeloTabela.getValueAt(i, 2).toString();
            
          
            Model.Produtos item = new Model.Produtos();
            item.setQuantidade(qtd);
            item.setNomeProduto(nomeProd);
            item.setUnidade(unidade);
            
            
            novoPedido.adicionarItem(item);
        }

        
        Controller.ExpedicaoController controller = new Controller.ExpedicaoController();
        controller.salvar(novoPedido);

       
        javax.swing.JOptionPane.showMessageDialog(this, "Pedido de expedição registrado com sucesso!");

        
        Telas.Dashboard dashboard = (Telas.Dashboard) javax.swing.SwingUtilities.getWindowAncestor(this);
        dashboard.abrirPainel(new PanelExpedicao.GerenciamentoExpedicao());
        
    }//GEN-LAST:event_btnSalvarPedidoActionPerformed

    private void btnAdicionaProdutoPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionaProdutoPedidoActionPerformed
        // TODO add your handling code here:
          // 1. VALIDAÇÃO DO COMBOBOX DE PRODUTO
        if (cbSelecionaProduto.getSelectedIndex() <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione um produto válido!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. VALIDAÇÃO DO CAMPO DE QUANTIDADE
        if (txtQuantidadeProdutosPedidos.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, informe a quantidade do produto!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. TENTATIVA DE CONVERSÃO DA QUANTIDADE PARA NÚMERO
        int qtdInformada = 0;
        try {
            qtdInformada = Integer.parseInt(txtQuantidadeProdutosPedidos.getText().trim());
            if (qtdInformada <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "A quantidade deve ser maior que zero!", "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, insira apenas números inteiros no campo QUANTIDADE!", "Erro de Digitação", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 4. DESCOBRIR A UNIDADE DO PRODUTO (Buscando no controlador de produtos)
        String produtoNome = cbSelecionaProduto.getSelectedItem().toString();
        String unidadeMedida = "UN"; // Valor padrão caso não encontre
        
        Controller.ProdutosController produtosController = new Controller.ProdutosController();
        for (Model.Produtos p : produtosController.listar()) {
            if (p.getNomeProduto().equalsIgnoreCase(produtoNome)) {
                unidadeMedida = p.getUnidade(); // Captura a unidade real cadastrada (KG, CX, UN...)
                break;
            }
        }

        // 5. INSERIR A LINHA DENTRO DA JTABLE DA TELA
        javax.swing.table.DefaultTableModel modeloTabela = (javax.swing.table.DefaultTableModel) tableProdutosPedidosAdc.getModel();
        
        Object[] novaLinha = new Object[]{
            qtdInformada,   // Coluna 1: Quantidade
            produtoNome,    // Coluna 2: Produto
            unidadeMedida   // Coluna 3: Unidade
        };
        
        modeloTabela.addRow(novaLinha);

        // 6. LIMPAR E PREPARAR O CAMPO PARA O PRÓXIMO PRODUTO
        txtQuantidadeProdutosPedidos.setText("");
        cbSelecionaProduto.setSelectedIndex(0);
    }//GEN-LAST:event_btnAdicionaProdutoPedidoActionPerformed


