    public final void atualizarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tablePedidos.getModel();
        modelo.setRowCount(0); // Limpa rascunhos visuais
        
        ExpedicaoController controller = new ExpedicaoController();
        ArrayList<Expedicao> lista = controller.listar();
        
        // Vincula o renderizador personalizado à coluna de índice 4 (Coluna "Status")
        tablePedidos.getColumnModel().getColumn(4).setCellRenderer(new StatusCellRenderer());
        
        // Contador para simular o número sequencial do Pedido
        int numeroPedido = 1; 
        
        for (Expedicao exp : lista) {
            // Unifica os nomes dos produtos e soma a quantidade total do pedido
            StringBuilder nomesProdutos = new StringBuilder();
            int qtdTotal = 0;
            
            for (Produtos p : exp.getItensPedido()) {
                if (nomesProdutos.length() > 0) nomesProdutos.append(", ");
                nomesProdutos.append(p.getNomeProduto());
                qtdTotal += p.getQuantidade();
            }
            
            Object[] linha = new Object[]{
                "#" + numeroPedido++,     // Pedido
                exp.getCliente(),         // Cliente
                nomesProdutos.toString(), // Produtos unificados
                qtdTotal,                 // Quantidade total calculada
                exp.getStatus(),          // Status (Será interceptado e pintado de azul)
                exp.getMotorista()        // Motorista
            };
            modelo.addRow(linha);
        }
    }

