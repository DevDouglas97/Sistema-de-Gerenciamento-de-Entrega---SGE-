    private void btnVisualizarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarPedidoActionPerformed
        // TODO add your handling code here:
        
        int linhaSelecionada = tablePedidos.getSelectedRow();
        
        
        if (linhaSelecionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, clique primeiro em um pedido na tabela e depois em VISUALIZAR PEDIDO!");
            return;
        }
        
       
        Controller.ExpedicaoController controller = new Controller.ExpedicaoController();
        Model.Expedicao pedidoSelecionado = controller.listar().get(linhaSelecionada);
        int numeroDoPedido = linhaSelecionada + 1;
        
       
        Telas.Dashboard dashboard = (Telas.Dashboard) javax.swing.SwingUtilities.getWindowAncestor(this);
        dashboard.abrirPainel(new VisualizarPedidos(pedidoSelecionado, numeroDoPedido));
    }//GEN-LAST:event_btnVisualizarPedidoActionPerformed


