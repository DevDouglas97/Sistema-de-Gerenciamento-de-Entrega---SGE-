     private void carregarClientes() {
        // 1. Instancia o controlador de clientes para pegar os dados da memória
        ClienteController clienteController = new ClienteController();
        ArrayList<Cliente> listaClientes = clienteController.listar();
        
        // 2. Cria um modelo novo para o ComboBox
        DefaultComboBoxModel<String> modeloCombo = new DefaultComboBoxModel<>();
        
        // 3. Adiciona uma mensagem de instrução como primeira opção
        modeloCombo.addElement("Selecione um Cliente...");
        
        // 4. Percorre a lista de clientes cadastrados e adiciona o nome de cada empresa no combo
        for (Cliente cliente : listaClientes) {
            modeloCombo.addElement(cliente.getNomeEmpresa());
        }
        
        // 5. Aplica o novo modelo preenchido ao seu ComboBox da tela
        cbSelecioneCliente.setModel(modeloCombo);
    }
