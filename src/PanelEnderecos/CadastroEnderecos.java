    private void btnAtribuirEnderecoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtribuirEnderecoActionPerformed
        // TODO add your handling code here:
        
        int indiceSelecionado = cbSelecioneCliente.getSelectedIndex();
        if (indiceSelecionado <= 0) { // 0 é o texto "Selecione um Cliente..."
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione um cliente válido!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        
        if (txtDescricao.getText().trim().isEmpty() || txtEndereco.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Os campos DESCRIÇÃO e ENDEREÇO são obrigatórios!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        
        Model.Endereco enderecoObj;
        if (this.enderecoExistente == null) {
            enderecoObj = new Model.Endereco(); // Novo Endereço
        } else {
            enderecoObj = this.enderecoExistente; // Edita o existente
        }

        
        enderecoObj.setCliente(cbSelecioneCliente.getSelectedItem().toString());
        enderecoObj.setDescricao(txtDescricao.getText().trim());
        enderecoObj.setEndereco(txtEndereco.getText().trim());
        enderecoObj.setBairro(txtBairro.getText().trim());
        enderecoObj.setCidadeUf(txtCidadeUf.getText().trim());

        
        if (this.enderecoExistente == null) {
            Controller.EnderecoController controller = new Controller.EnderecoController();
            controller.salvar(enderecoObj);
        }

        
        javax.swing.JOptionPane.showMessageDialog(this, "Endereço gravado com sucesso!");

        
        Telas.Dashboard dashboard = (Telas.Dashboard) javax.swing.SwingUtilities.getWindowAncestor(this);
        dashboard.abrirPainel(new PanelEnderecos.GerenciamentoEnderecos());
    }//GEN-LAST:event_btnAtribuirEnderecoActionPerformed

